#!/usr/bin/env python3
def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Args:
        arr1 (list of int/float): The first array.
        arr2 (list of int/float): The second array.

    Returns:
        list of int/float: A new list containing the elements of arr1 followed by the elements of arr2.
    """
    return arr1 + arr2
